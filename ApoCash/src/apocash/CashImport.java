    //method untuk memuat data dalam betuk excel dengan format xls
    private void muatData() {
        Vector headers = new Vector();
        Vector data = new Vector();
        jfc = new JFileChooser();
        //melakukan fungsi pilih file
        jfc.setDialogTitle("Pilih File Excel");
        if (jfc.showOpenDialog(jPanel1) == JFileChooser.APPROVE_OPTION) {
            file = jfc.getSelectedFile();
            // buat model untuk file excel
            // jika filenya ada
            if (file.exists()) {
                // maka jalankan perintah berikut
                try {
                    Workbook workbook = Workbook.getWorkbook(file);
                    Sheet sheet = workbook.getSheets()[0];

                    //untuk baris pertama akan dianggap sebagai header table
                    headers.clear();
                    for (int i = 0; i < sheet.getColumns(); i++) {
                        Cell cell1 = sheet.getCell(i, 0);
                        headers.add(cell1.getContents());
                    }
                    // untuk baris selanjutnya akan dianggap sebagai isi dari table
                    data.clear();
                    for (int j = 1; j < sheet.getRows(); j++) {
                        Vector d = new Vector();
                        for (int i = 0; i < sheet.getColumns(); i++) {
                            Cell cell = sheet.getCell(i, j);
                            d.add(cell.getContents());
                        }
                        d.add("\n");
                        data.add(d);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Error: " + e);
                }
                //lakukan perintah membuat table berdasarkan header dan isi yang sudah dibentuk
                DefaultTableModel model = new DefaultTableModel(data, headers);
                jTable1.setModel(model);
                jTable1.setAutoCreateRowSorter(true);
                model = new DefaultTableModel(data, headers);
                jTable1.setModel(model);
            } else {
                JOptionPane.showMessageDialog(null, "File does not exist");
            }
        }
    }
    
