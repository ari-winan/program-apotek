    //button import data ke dalam table obat
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {

            //perintah untuk mengakses database apocash
            int rows = jTable1.getRowCount();
            Class.forName("com.mysql.jdbc.Driver").newInstance();
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/apocash", "root", "");
            conn.setAutoCommit(false);

            //query untuk melakukan insert data dalam table obat
            String queryco = "Insert into obat(kode_obat,nama_obat,satuan,stok,harga_kontrak,harga_jual) values (?,?,?,?,?,?)";
            pst = conn.prepareStatement(queryco);
            for (int row = 0; row < rows; row++) {
                //perintah untuk mendapatkan value dalam setiap baris table
                String kode = (String) jTable1.getValueAt(row, 0);
                String nama = (String) jTable1.getValueAt(row, 1);
                String satuan = (String) jTable1.getValueAt(row, 2);
                String stok = (String) jTable1.getValueAt(row, 3);
                String kontrak = (String) jTable1.getValueAt(row, 4);
                String jual = (String) jTable1.getValueAt(row, 5);
                
                //Statement untuk dapat melakukan insert setelah value dari data didapatkan
                pst.setString(1, kode);
                pst.setString(2, nama);
                pst.setString(3, satuan);
                pst.setInt(4, Integer.parseInt(stok));
                pst.setInt(5, Integer.parseInt(kontrak));
                pst.setInt(6, Integer.parseInt(jual));
                pst.addBatch();
            }
            pst.executeBatch();
            conn.commit();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

